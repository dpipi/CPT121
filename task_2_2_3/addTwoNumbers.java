package task_2_2_3;

public class addTwoNumbers {

    public static void main(String[] args) {
        System.out.println(addTwo(5.5, 6.6));
    }

    public static int addTwo(int a, int b) {
        return a + b;

    }

    public static double addTwo(double a, double b) {
        return a + b;

    }

}